package com.home.model;

public class MonthlyReviewEntity {
    
    private String review_id;
    private String log_in_id;
    private String month_name;
    private String month_income;
    private String month_expense;
    private String month_saving;
    private String month_ratio;

    public String getLog_in_id() {
        return log_in_id;
    }

    public void setLog_in_id(String log_in_id) {
        this.log_in_id = log_in_id;
    }

    public String getReview_id() {
        return review_id;
    }

    public void setReview_id(String review_id) {
        this.review_id = review_id;
    }



    public String getMonth_name() {
        return month_name;
    }

    public void setMonth_name(String month_name) {
        this.month_name = month_name;
    }

    public String getMonth_income() {
        return month_income;
    }

    public void setMonth_income(String month_income) {
        this.month_income = month_income;
    }

    public String getMonth_expense() {
        return month_expense;
    }

    public void setMonth_expense(String month_expense) {
        this.month_expense = month_expense;
    }

    public String getMonth_saving() {
        return month_saving;
    }

    public void setMonth_saving(String month_saving) {
        this.month_saving = month_saving;
    }

    public String getMonth_ratio() {
        return month_ratio;
    }

    public void setMonth_ratio(String month_ratio) {
        this.month_ratio = month_ratio;
    }

    
   
    
}
